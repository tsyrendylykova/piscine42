#include "do_op.h"

int	ft_sum(int a, int b)
{
	return (a + b);
}

int	ft_subtract(int a, int b)
{
	return (a - b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}

int	ft_mul(int a, int b)
{
	return (a * b);
}
